package com.joedev.fanbible;

public class Cities {
	private int id;
	private String city;
	
	public void set_id(int id){
		this.id = id;
	}
	
	public void set_city(String city){
		this.city = city;
	}
	
	public int get_id(){
		return this.id;
	}
	
	public String get_city(){
		return this.city;
	}
}
